bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case EMOJI:
      if (record->event.pressed) {
        tap_code16(C(G(KC_SPC)));
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if(IS_LAYER_ON(_RAISE)){
            if (clockwise) {
                tap_code(KC_MS_WH_UP);
            } else {
                tap_code(KC_MS_WH_DOWN);
            }
        }
        else if(IS_LAYER_ON(_LOWER)){
            if (clockwise) {
                tap_code(KC_BRMU);
            } else {
                tap_code(KC_BRMD);
            }
        }
        else{
            if (clockwise) {
                tap_code(KC_MS_RIGHT);
            } else {
                tap_code(KC_MS_LEFT);
            }
        }
    }
    else if (index == 1) {
        if(IS_LAYER_ON(_LOWER)){
            if (clockwise) {
                tap_code(KC_VOLU);
            } else {
                tap_code(KC_VOLD);
            }
        }
        else{
            if (clockwise) {
                tap_code(KC_MS_UP);
            } else {
                tap_code(KC_MS_DOWN);
            }
        }
    }
}
